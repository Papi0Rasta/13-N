#include <unistd.h>

int	main(int argc, char **argv){
	int j = argc - 1;
	int i;
	while(j > 0){
		i=0;
		while(argv[j][i])
			i++;
		write(1, argv[j],i);
		write(1,"\n",1);
		j--;
	}
	return 0;
}
