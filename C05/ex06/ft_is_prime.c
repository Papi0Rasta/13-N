#include <unistd.h>

int ft_is_prime(int nb){
	int i=2;
	if(nb < 2)
		return 0;
	while(i < nb){
		if(nb % i == 0){
			return 0;
		}
		i++;
	}
	return 1;
}

int	main(){
	char p = ft_is_prime(2) + '0';
	write(1,&p,1);
	return 0;
}
