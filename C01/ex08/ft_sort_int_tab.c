#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size){
	int i=0;
	int j;
	int n;
	while(i < size){
		j=0;
		while(j < size){
			if(tab[j] > tab[j+1]){
				n = tab[j];
				tab[j] = tab[j+1];
				tab[j+1] = n;
			}
			j++;
		}
		i++;
	}
}
