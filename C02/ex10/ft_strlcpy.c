unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size){
	int i=0;
	while(src[i] && i < size){
		dest[i] = src[i];
		i++;
	}
	
	dest[i] = '\0';
	while(i < size){
		i++;
	}
	return i;
}
