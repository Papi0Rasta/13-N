char	*ft_strcat(char *dest, char *src){
	int i=0;
	int j=0;
	while(*dest){
		i++;
	}
	while(*src){
		dest[i+j] = src[j];
		j++;
	}
	dest[i+j] = '\0';
	return dest;
}
